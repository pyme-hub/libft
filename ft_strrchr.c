#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*m;

	c = c % 256;
	m = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			m = ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (m);
}
